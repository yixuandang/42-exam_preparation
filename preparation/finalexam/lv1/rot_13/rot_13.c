#include "rot_13.h"

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	else
	{
		while (argv[1][i] != '\0')
		{
			rot_13(i, argv);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
